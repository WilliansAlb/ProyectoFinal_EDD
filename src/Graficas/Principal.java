    public void login() {
        JPanel panel = new JPanel(new BorderLayout(5, 5));

        JPanel label = new JPanel(new GridLayout(0, 1, 2, 2));
        label.add(new JLabel("ID", SwingConstants.RIGHT));
        label.add(new JLabel("PASSWORD", SwingConstants.RIGHT));
        panel.add(label, BorderLayout.WEST);

        JPanel controls = new JPanel(new GridLayout(0, 1, 2, 2));
        JTextField username = new JTextField();
        controls.add(username);
        JPasswordField password = new JPasswordField();
        controls.add(password);
        panel.add(controls, BorderLayout.CENTER);

        JOptionPane.showMessageDialog(this, panel, "Login", JOptionPane.QUESTION_MESSAGE);
        String regex = "\\d+";
        if (username.getText().matches(regex)) {
            int id = Integer.parseInt(username.getText());
            Usuario s = (Usuario) contenedor.usuarios.obtener(id);
            if (s != null) {
                if (s.getPassword().equals(new String(password.getPassword()))) {
                    if (s.getTipo() == 0 || s.getTipo() == 2) {
                        tab_global.setEnabledAt(1, true);
                        tab_global.setEnabledAt(2, true);
                        tab_global.setEnabledAt(3, true);
                        tab_global.setEnabledAt(4, true);
                        tab_global.setEnabledAt(5, true);
                        tab_global.setEnabledAt(6, true);
                        tab_global.setEnabledAt(7, true);
                        btn_cargar.setText("CERRAR SESION");
                        jPanel28.setVisible(true);
                    } else {
                        tab_global.setEnabledAt(1, false);
                        tab_global.setEnabledAt(2, false);
                        tab_global.setEnabledAt(3, false);
                        tab_global.setEnabledAt(4, false);
                        tab_global.setEnabledAt(5, false);
                        tab_global.setEnabledAt(6, false);
                        tab_global.setEnabledAt(7, true);
                        btn_cargar.setText("CERRAR SESION");
                        jPanel28.setVisible(false);
                        limpiar_tabla(tabla_cursos_asignados);
                        SimpleEnlazada horarios = contenedor.horarios.listado();
                        if (horarios.getRaiz() != null) {
                            SimpleEnlazada.NodoSimple it = horarios.getRaiz();
                            while (it != null) {
                                Horario tem = (Horario) it.getData();
                                if (tem != null) {
                                    Circular.NodoCircular n = tem.getAsignaciones().raiz;
                                    if (n != null) {
                                        do {
                                            if (n.getData() instanceof Asignacion) {
                                                Asignacion t = (Asignacion) n.getData();
                                                if (t != null && t.getEstudiante().getCarnet() == id) {
                                                    DefaultTableModel mo = (DefaultTableModel) tabla_cursos_asignados.getModel();
                                                    mo.addRow(new Object[]{tem.getCurso().getCodigo(), tem.getCurso().getNombre(), tem.getRango(), tem.getCodigo(), t.getZona(), t.getPunteo_final()});
                                                }
                                            }
                                            n = n.getSiguiente();
                                        } while (n != tem.getAsignaciones().raiz);
                                    }
                                }
                                it = it.getSiguiente();
                            }
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No coinciden las credenciales");
                }
            } else {
                JOptionPane.showMessageDialog(null, "El ID que ingresaste no existe");
            }
        } else {
            JOptionPane.showMessageDialog(null, "El ID debe ser un numero");
        }
    }

    public void generar_grafica(String nombre, JLabel referencia) {
        String path2 = System.getProperty("user.dir");

        String decodedPath = "";
        try {
            decodedPath = URLDecoder.decode(path2, "UTF-8");
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            String dotPath = "dot";

            String fileInputPath = decodedPath + "/src/TextoGraphviz/" + nombre + ".txt";
            String fileOutputPath = decodedPath + "/src/Graphviz/" + nombre + ".jpg";

            String tParam = "-Tjpg";
            String tOParam = "-o";

            String[] cmd = new String[5];
            cmd[0] = dotPath;
            cmd[1] = tParam;
            cmd[2] = fileInputPath;
            cmd[3] = tOParam;
            cmd[4] = fileOutputPath;

            Runtime rt = Runtime.getRuntime();

            Process p = rt.exec(cmd);
            p.waitFor();
            ImageIcon n = new ImageIcon(decodedPath + "/src/Graphviz/" + nombre + ".jpg");
            if (n.getIconHeight() > n.getIconWidth()) {
                ImageIcon imageIcon = new ImageIcon(new ImageIcon(decodedPath + "/src/Graphviz/" + nombre + ".jpg").getImage().getScaledInstance(-1, referencia.getHeight(), Image.SCALE_SMOOTH));

                referencia.setIcon(imageIcon);
            } else {
                ImageIcon imageIcon = new ImageIcon(new ImageIcon(decodedPath + "/src/Graphviz/" + nombre + ".jpg").getImage().getScaledInstance(referencia.getWidth(), -1, Image.SCALE_SMOOTH));

                referencia.setIcon(imageIcon);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

